export const THEME_STORAGE_KEY = 'komun_theme';

export function buildThemeColors(theme: Theme): Record<string, string> {
	const c = theme.colors;
	const isLight = bestContrast(c.bg) === '#000000';
	const adjust = isLight ? darken : lighten;
	return {
		...c,
		font: THEME_FONTS[theme.name] || "'Inter', sans-serif",
		'accent-soft': soft(c.accent, 0.125),
		'kind-need-soft': soft(c['kind-need'] || c.accent, 0.125),
		'kind-offer-soft': soft(c['kind-offer'] || c.success, 0.125),
		'kind-resource-soft': soft(c['kind-resource'] || '#818cf8', 0.125),
		'success-soft': soft(c.success, 0.125),
		'success-softer': soft(c.success, 0.08),
		'success-strong': soft(c.success, 0.25),
		'warning-soft': soft(c.warning, 0.125),
		'critical-soft': soft(c.critical, 0.125),
		'critical-softer': soft(c.critical, 0.06),
		overlay: 'rgba(0, 0, 0, 0.5)',
		'text-on-accent': bestContrast(c.accent),
		'text-on-success': bestContrast(c.success),
		'text-on-warning': bestContrast(c.warning),
		'text-on-critical': bestContrast(c.critical),
		'text-on-need': bestContrast(c['kind-need'] || c.accent),
		'text-on-offer': bestContrast(c['kind-offer'] || c.success),
		'text-on-resource': bestContrast(c['kind-resource'] || '#818cf8'),
		// Semantic interactives
		'button-primary-bg': c.accent,
		'button-primary-hover': adjust(c.accent, 8),
		'button-primary-active': adjust(c.accent, 14),
		'button-primary-text': bestContrast(c.accent),
		'button-ghost-hover': soft(c.accent, 0.08),
		'button-ghost-active': soft(c.accent, 0.14),
		'input-focus-ring': soft(c.accent, 0.25),
		radius: '8px',
		'radius-lg': '12px',
	};
}

export function themeBySystem(): string {
	if (typeof window === 'undefined') return 'Komun Dark';
	if (window.matchMedia('(prefers-color-scheme: light)').matches) {
		return 'Komun Light';
	}
	return 'Komun Dark';
}
